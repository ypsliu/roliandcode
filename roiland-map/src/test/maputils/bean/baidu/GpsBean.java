public class GpsBean {
	
	/**
	 * 经度
	 */
	private double x;
	
	/**
	 * 纬度
	 */
	private double y;

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}
}
